            <h2>2. Potatoes</h2>
            <p>
             Potatoes are one of nature’s most nourishing and nutrient rich carbohydrates. One medium potato (that's about the size of your fist) has about 110 calories and nearly half your day’s requirement for vitamin C, along with potassium (more than a banana), vitamin B6, magnesium, and a hefty dose of fiber when you eat the skin. While potatoes get a bad rap for being high in calories, it’s only because they’re often loaded with extras like cheese, salt, sour cream, and bacon, which pack on added fats and oils. Whether baked, boiled, or mashed, potatoes on their own are a satisfying starchy carbohydrate, especially for those watching their weight. Both white and sweet potatoes are nutrient rich, but sweet potatoes have the added benefit of extra vitamin A in a sweet potato. Don't forget, potatoes are also free of gluten.</p>
