            <h2>7. Apples</h2>
            <p>
              Does an apple a day really keep the doctor away? Science seems to think so! Apples are loaded with vitamins, minerals, fiber, and water. They are rich in antioxidants and flavonoids — the polyphenol compounds also found in grapes and red wine thought to boost heart health. With about 100 calories in a medium apple (about the size of a baseball) apples are a boon to dieters because the crunch provides a lot of eating satisfaction and they're nice and sweet.</p>

              <p>For optimal nutrients, don't forget to eat the skin, where many of the nutrients are found. Produced worldwide, fresh apples are available year-round, with many different varieties. Plus, apples are delicious raw or cooked and with so many varieties available, they never get boring.</p>

