            <h2>3. Flank Steak</h2>
            <p>
            Red meat can be an important part of a healthy diet when the meat is lean and served in small portions. There are more than 25 cuts of lean red meat available and flank steak is among the most popular for its taste and affordability. As a lean red meat, flank steak is a super source of vitamin B12 and iron — both major nutrients to prevent anemia. A 4-ounce serving is also rich in protein with about 25 to 30 grams of high quality, complete protein. A modest serving, about the size of a computer mouse, can support muscle tissue, brain function, blood cells and the immune system, among other systems. Processed and fatty red meats like sausage are not part of the healthy picture and you shouldn't be eating huge portion sizes (like 12 to 16-ounce serving), as extensive studies have demonstrated. But flank steak, which doesn't contain a lot of fat, is more economical because it’s not tender and must be marinated prior to grilling.</p>
