            <h2>4. Low-Fat Yogurt</h2>
            <p>
            Yogurt comes from milk and contains the same set of nutrients, plus more. A 6-ounce serving of plain, low-fat yogurt has about 9 grams of complete protein (or double that amount for strained, Greek-style yogurt), calcium, vitamin B-12, potassium, and magnesium. One serving of yogurt has about one third of your calcium needs for the day! Yogurt, like milk, is also fortified with vitamin D.</p>

            <p> 
            While these nutrients support strong bones and teeth, yogurt is also a natural source of probiotics — healthy bacteria that support a healthy digestive track. Probiotics are also thought to contribute to a healthy immune system. Buy a large tub of plain yogurt, scoop out a single serving, and add fruits, nuts, or other low-sugar toppings for variety. Yogurt can be eaten at any meal, as a snack, and as a swap for high-fat sour cream in recipes. Stick with low fat or non-fat yogurt to limit artery-clogging saturated fat from any full fat dairy product. Keep in mind that many people who are lactose intolerant who lack the enzyme to digest milk, can digest one serving of yogurt a day. That’s because the healthy bacteria in yogurt contains lactase, the enzyme needed to digest lactose in milk.</p>
            <br>
            <h2>5. Carrots</h2>
            <p>  
            Colorful vegetables are a mainstay of healthy eating. And carrots are plentiful and economical, plus they're available year-round. They are also a powerhouse of beta-carotene (vitamin A), and fiber. Like most vegetables, they are also high in water, which helps support hydration. Carrots are crunchy and sweet, so they're veggie accepted by children and adults alike. It’s often one of the first foods for babies, because of its taste and texture.</p>

            <p>A versatile veggie, carrots can be eaten raw, cooked, in a smoothie or soup and as the base of side dishes to support other, less popular veggies (like peas). While mini-carrots look like “baby” carrots, these are actually whittled down pieces of very large carrots. You can find true baby carrots, which are picked when small for a tender treat, but they are pricey and a specialty item. Stick with regular carrots in the bag for a sweeter taste and low price point. While organic and conventional carrots both contain the same nutrients, wash both types thoroughly before eating. And peel the carrots if you’re concerned about any dirt particles on the skin, even if you lose a bit of fiber.</p>
            <br>
            <h2>6. Nuts</h2>
            <p>
              Few foods can beat tree nuts for a nutritious punch of protein and heart-healthy fats. Nuts are a good source of omega-3-fats, fiber, vitamin E, and the amino acid arginine. Together, these nutrients support heart health and circulation, as well as both muscle and brain function. A superb plant protein, tree nuts — like almonds, walnuts, cashews, hazelnuts, pistachio nuts, and Brazil nuts — are very versatile.</p>

              <p>A perfect snack, garnish for vegetables, mashed into a “burger,” or used as an oil, nuts are economical and available everywhere. It’s often hard to control portions, so try pistachio nuts in the shell, where a serving is about 39 nuts, with about 160 calories. Because nuts are so calorie-dense, it’s best to limit to one or two servings a day. Keep in mind that adding nuts as a source of heart healthy fat won’t do your heart any good if artery-clogging fats (like those found in dairy and meat) are not reduced.</p>
